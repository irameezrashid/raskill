<script>
  // Delegated listener for address buttons (keeps HTML clean)
  document.addEventListener('click', function (e) {
    const btn = e.target.closest('button[data-location]');
    if (!btn) return;
    const location = btn.getAttribute('data-location');
    if (location) {
      // call your existing openMap function (modal code) safely
      if (typeof openMap === 'function') {
        openMap(location);
      } else {
        // fallback: open Google Maps in new tab
        window.open(`https://www.google.com/maps?q=${encodeURIComponent(location)}`, '_blank');
      }
    }
  });

  // Optional: keyboard activation support (Space/Enter) for non-button elements if needed
  // (Not necessary for <button>, included for completeness)
  document.addEventListener('keydown', function (e) {
    if ((e.key === 'Enter' || e.key === ' ') && document.activeElement?.dataset?.location) {
      document.activeElement.click();
      e.preventDefault();
    }
  });
</script>
